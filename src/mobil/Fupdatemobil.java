    private void autonumber(){
        try{
            Connection c = koneksi.getkoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM mobil ORDER BY id_mobil DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String NoID = r.getString("id_mobil").substring(2);
                String ID = ""+(Integer.parseInt(NoID)+1);
                String Zero = "";
                
                if (ID.length()==1) 
                    {Zero = "0";}
                    else if (ID.length()==2)
                     {Zero = "";}
                txtIdMobil.setText("M" + Zero + ID);
                } else {
                txtIdMobil.setText("M01");
                }
                r.close();
                s.close();
        } catch (Exception e){
            System.out.println("Autonumber Error");
        }
    }
    
    public void tampilNama(){
        try {
            //Buat Koneksi Ke jdbc
             Class.forName("com.mysql.jdbc.Driver");
             Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db10117122penyewaan", "root","");
             
             //Membuka Query
             Statement stm = conn.createStatement();
             
             //Membuat SQL
             String sql = "SELECT * FROM depot ORDER BY nama_depot ASC";
             
             //Eksekusi Query
             ResultSet rs = stm.executeQuery(sql);
             
             while(rs.next()){
                 Object[] ob = new Object[1];
                 ob[0] = rs.getString("nama_depot");
                 cmbNamaDepot.addItem((String) ob[0]);
             }
             rs.close();
             stm.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error Karena "+ e);
        }  
    }
    
    public void tampilId(){
        try {
            //Buat Koneksi Ke jdbc
             Class.forName("com.mysql.jdbc.Driver");
             Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db10117122penyewaan", "root","");
             
             //Membuka Query
             Statement stm = conn.createStatement();
             
             //Membuat SQL
             String sql = "SELECT * FROM depot WHERE nama_depot='"+cmbNamaDepot.getSelectedItem()+"'";
             
             //Eksekusi Query
             ResultSet rs = stm.executeQuery(sql);
             
             while(rs.next()){
                 Object[] ob = new Object[1];
                 ob[0] = rs.getString("id_depot");
                 
                 //Set Data Ke Textfield
                 txtIdDepot.setText((String) ob[0]);
             }
             rs.close();
             stm.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error Karena "+ e);
        }
    }
