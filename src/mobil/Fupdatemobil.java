    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (txtIdDepot.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "ID Depot Tidak Boleh Kosong!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (txtNoPolisi.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "No Polisi Tidak Boleh Kosong!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (txtMerk.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Merk Tidak Boleh Kosong!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (tahunBuat.getDate().equals("")) {
        JOptionPane.showMessageDialog(null, "Tanggal Pinjam Tidak Boleh Kosong!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (txtIsiSilinder.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Tanggal Kembali Tidak Boleh Kosong!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }else if (txtWarna.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Tanggal Kembali Tidak Boleh Kosong!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }else if (txtBahanBakar.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Tanggal Kembali Tidak Boleh Kosong!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }else if (txtStatus.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Tanggal Kembali Tidak Boleh Kosong!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }else if (txtHargaSewa.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Tanggal Kembali Tidak Boleh Kosong!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } 
        else {
        JOptionPane.showMessageDialog(null, "Data Berhasil Di Simpan!!");
        }
        String tampilan = "yyyy-MM-dd";
        SimpleDateFormat fm = new SimpleDateFormat(tampilan);
        String tanggal = String.valueOf(fm.format(tahunBuat.getDate()));
        DBmobil db = new DBmobil();
        db.update_mobil(new mobil(txtIdMobil.getText(), txtIdDepot.getText(), txtNoPolisi.getText(), txtMerk.getText(), tanggal,
                txtIsiSilinder.getText(), txtWarna.getText(), txtBahanBakar.getText(), txtStatus.getText(), 
                Integer.valueOf(txtHargaSewa.getText())));
        setVisible(false);
    }//GEN-LAST:event_btnSimpanActionPerformed

